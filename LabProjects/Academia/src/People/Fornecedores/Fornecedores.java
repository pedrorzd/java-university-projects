package People.Fornecedores;

public class Fornecedores {
    private int codeFornecedor;
    private String nomeFornecedor;
    private String mercadoria;
    private String emailFornecedor;
    private String CNPJ;

    public int getCodeFornecedor() {
        return codeFornecedor;
    }

    public void setCodeFornecedor(int codeFornecedor) {
        this.codeFornecedor = codeFornecedor;
    }

    public String getNomeFornecedor() {
        return nomeFornecedor;
    }

    public void setNomeFornecedor(String nomeFornecedor) {
        this.nomeFornecedor = nomeFornecedor;
    }

    public String getMercadoria() {
        return mercadoria;
    }

    public void setMercadoria(String mercadoria) {
        this.mercadoria = mercadoria;
    }

    public String getEmailFornecedor() {
        return emailFornecedor;
    }

    public void setEmailFornecedor(String emailFornecedor) {
        this.emailFornecedor = emailFornecedor;
    }

    public String getCNPJ() {
        return CNPJ;
    }

    public void setCNPJ(String CNPJ) {
        this.CNPJ = CNPJ;
    }

    public String toString() {
        return "\nFornecedor: \n"+
                "\ncodigo: " + getCodeFornecedor()+
                "\nNome fornecedor: " + getNomeFornecedor()+
                "\nEmail: " + getEmailFornecedor()+
                "\nMercadoria: "+ getMercadoria()+
                "\nCNPJ: "+getCNPJ();

    }

}
