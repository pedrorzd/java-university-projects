package People.Colaboradores;

public class Colaboradores {
    private int codeColaborador;
    private String nomeColaborador;
    private String celularColaborador;
    private String funcao;
    private double salario;

    public int getCodeColaborador() {
        return codeColaborador;
    }

    public void setCodeColaborador(int codeColaborador) {
        this.codeColaborador = codeColaborador;
    }

    public String getNomeColaborador() {
        return nomeColaborador;
    }

    public void setNomeColaborador(String nomeColaborador) {
        this.nomeColaborador = nomeColaborador;
    }

    public String getCelularColaborador() {
        return celularColaborador;
    }

    public void setCelularColaborador(String celularColaborador) {
        this.celularColaborador = celularColaborador;
    }

    public String getFuncao() {
        return funcao;
    }

    public void setFuncao(String funcao) {
        this.funcao = funcao;
    }

    public double getSalario() {
        return salario;
    }

    public void setSalario(double salario) {
        this.salario = salario;
    }

    public String toString() {
        return "\nColaborador: \n"+
                "\ncodigo: " + getCodeColaborador()+
                "\nNome: " + getNomeColaborador()+
                "\nCelular: " + getCelularColaborador()+
                "\nFunção: "+ getFuncao()+
                "\nSalário: "+ getSalario();
    }
}
